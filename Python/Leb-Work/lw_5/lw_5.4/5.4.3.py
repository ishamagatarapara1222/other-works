#3. Show how len() behaves polymorphically with a string, a list, and a dictionary.

my_string = "Python"
my_list = [10, 20, 30, 40]
my_dict = {"name": "Alice", "age": 20, "course": "AI/ML"}


print("Length of string:", len(my_string))
print("Length of list:", len(my_list))
print("Length of dictionary:", len(my_dict))
