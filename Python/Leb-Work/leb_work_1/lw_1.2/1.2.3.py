'''
Q.3

Perform:

Addition
Subtraction
Multiplication
Division
Floor division
Modulus
Exponentiation
(on two numbers input by the user)
'''

a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

print("Addition = ", a+b)
print("Subtraction = ", a-b)
print("Multiplication = ", a*b)
print("Division = " , a/b)
print("Floor division = ", a // b)
print("Modulus = ", a%b)
print("Exponentiation = ", a ** b)
